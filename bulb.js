// turn on function 

function bulbOn() {
    let Bulbobj = document.querySelector(".bulb-off")
    Bulbobj.setAttribute("src", "bulb-on.png")
}

// turn off function 
function bulbOff() {
    let Bulbobj = document.querySelector(".bulb-off")
    Bulbobj.setAttribute("src", "bulb-off.png")
}